export function getRandomTheoryQuizQuestion(excludeId?: string): TheoryQuizQuestion {
    const pool = excludeId
        ? THEORY_QUIZ_QUESTIONS.filter((question) => question.id !== excludeId)
        : THEORY_QUIZ_QUESTIONS;

    const safePool = pool.length > 0 ? pool : THEORY_QUIZ_QUESTIONS;
    const randomIndex = Math.floor(Math.random() * safePool.length);
    return safePool[randomIndex];
}
